// Фолбэк без ИИ: ищем самый похожий вопрос по ключевым словам.
export function matchFAQ(text) {
  const t = text.toLowerCase();
  let best = null;
  let bestScore = 0;
  for (const item of FAQ) {
    const words = (item.q + " " + item.a).toLowerCase().split(/[^а-яёa-z0-9]+/);
    const score = words.filter((w) => w.length > 3 && t.includes(w)).length;
    if (score > bestScore) { bestScore = score; best = item; }
  }
  return bestScore >= 1 ? best.a : null;
}
